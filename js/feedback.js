function checkForm()
{
    $('#errors').html('');
    var name = $('input[name=name]').val();
    var email = $('input[name=email]').val();
    var feedback = $('textarea[name=feedback]').val();
    var valid = true;
    if(name.length > 20 || name.length < 2)
    {
        $('#errors').append('<div>* Invalid name. Please enter a valid name having a length between 2 and 20.</div>');
        valid = false;
    }            
    if(!isValidEmailAddress(email))
    {
        $('#errors').append('<div>* Invalid email. Please enter a valid email address.</div>');
        valid = false;
    }          
    if(feedback.length > 400 || feedback.length < 4)
    {
        $('#errors').append('<div>* Feedback length should be between 4 and 400 characters. Thank you.</div>');
        valid = false;
    }
    if(valid)
    {
        document.forms[0].submit();
    }
        
}
